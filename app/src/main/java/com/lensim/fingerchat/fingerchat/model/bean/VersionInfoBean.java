package com.lensim.fingerchat.fingerchat.model.bean;

public class VersionInfoBean {

    private int rowId;
    private String appId;
    private String appClient;
    private String appName;
    private String appVersion;
    private String appUrl;
    private int appState;
    private String appMsg;
    private long releaseTime;
    private String releaseMd5;
    private String hotver;
    private String hoturl;

    public int getRowId() {
        return rowId;
    }

    public void setRowId(int rowId) {
        this.rowId = rowId;
    }

    public String getAppId() {
        return appId;
    }

    public void setAppId(String appId) {
        this.appId = appId;
    }

    public String getAppClient() {
        return appClient;
    }

    public void setAppClient(String appClient) {
        this.appClient = appClient;
    }

    public String getAppName() {
        return appName;
    }

    public void setAppName(String appName) {
        this.appName = appName;
    }

    public String getAppVersion() {
        return appVersion;
    }

    public void setAppVersion(String appVersion) {
        this.appVersion = appVersion;
    }

    public String getAppUrl() {
        return appUrl;
    }

    public void setAppUrl(String appUrl) {
        this.appUrl = appUrl;
    }

    public int getAppState() {
        return appState;
    }

    public void setAppState(int appState) {
        this.appState = appState;
    }

    public String getAppMsg() {
        return appMsg;
    }

    public void setAppMsg(String appMsg) {
        this.appMsg = appMsg;
    }

    public long getReleaseTime() {
        return releaseTime;
    }

    public void setReleaseTime(long releaseTime) {
        this.releaseTime = releaseTime;
    }

    public String getReleaseMd5() {
        return releaseMd5;
    }

    public void setReleaseMd5(String releaseMd5) {
        this.releaseMd5 = releaseMd5;
    }

    public String getHotver() {
        return hotver;
    }

    public void setHotver(String hotver) {
        this.hotver = hotver;
    }

    public String getHoturl() {
        return hoturl;
    }

    public void setHoturl(String hoturl) {
        this.hoturl = hoturl;
    }
}
