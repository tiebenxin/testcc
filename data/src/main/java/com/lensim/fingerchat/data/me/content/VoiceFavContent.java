public class VoiceFavContent {
    private String userHeadImageStr;
    private String signContent;
    private String messageType;
    private String voiceLenth;
    private String type;
    private String userName;
    private String recordTime;
    private String content;

    public String getUserHeadImageStr() {
        return userHeadImageStr;
    }

    public void setUserHeadImageStr(String userHeadImageStr) {
        this.userHeadImageStr = userHeadImageStr;
    }

    public String getSignContent() {
        return signContent;
    }

    public void setSignContent(String signContent) {
        this.signContent = signContent;
    }

    public String getMessageType() {
        return messageType;
    }

    public void setMessageType(String messageType) {
        this.messageType = messageType;
    }

    public String getVoiceLenth() {
        return voiceLenth;
    }

    public void setVoiceLenth(String voiceLenth) {
        this.voiceLenth = voiceLenth;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getRecordTime() {
        return recordTime;
    }

    public void setRecordTime(String recordTime) {
        this.recordTime = recordTime;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
